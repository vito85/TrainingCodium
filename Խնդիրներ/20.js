// Написать функцию, которая принимает число и находит сумму всех простых чисел
//  до этого числа. Например, если входное число 10, то функция должна вернуть 17
//   (простые числа до 10 - 2, 3, 5, 7).

function sumOfPrimes(n) {
    let isPrime = new Array(n + 1).fill(true); // массив для хранения информации о простых числах
    isPrime[0] = isPrime[1] = false; // 0 и 1 не являются простыми числами
    let primes = []; // массив для хранения найденных простых чисел
  
    // проходим по всем числам до n
    for (let i = 2; i <= n; i++) {
      if (isPrime[i]) { // если i является простым числом
        primes.push(i); // добавляем i в массив простых чисел
        
        // отмечаем все кратные i числа как составные
        for (let j = i * i; j <= n; j += i) {
          isPrime[j] = false;
        }
      }
    }
    
    // возвращаем сумму всех простых чисел
    return primes.reduce((sum, current) => sum + current, 0);
  }

